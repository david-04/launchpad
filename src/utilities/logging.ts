import { SEPARATOR_LINE } from "./constants.js";
//----------------------------------------------------------------------------------------------------------------------
// Constants
//----------------------------------------------------------------------------------------------------------------------

import { breakLine } from "./string-utilities.js";

//----------------------------------------------------------------------------------------------------------------------
// Generate a separator
//----------------------------------------------------------------------------------------------------------------------

export function createSeparator(title: string) {
    return [SEPARATOR_LINE, title, SEPARATOR_LINE, ""] as const;
}

//----------------------------------------------------------------------------------------------------------------------
// Break and log a message
//----------------------------------------------------------------------------------------------------------------------

export function breakAndLog(...message: ReadonlyArray<string>) {
    breakLine(message.join("")).forEach(line => console.log(line));
}
